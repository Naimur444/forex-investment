  <script>
const containers = document.querySelectorAll('.scv');

// loop through each container and apply the scrolling animation
containers.forEach(container => {
  // get the height of the container and the last child element
  const containerHeight = container.clientHeight;
  const lastChildHeight = container.lastElementChild.clientHeight;

  // set the initial scroll position
  let scrollTop = 0;

  // define a function to update the scroll position
  function updateScrollPosition() {
    // update the scroll position
    scrollTop += 1;
    if (scrollTop >= containerHeight - lastChildHeight) {
      scrollTop = 0;
    }
    container.scrollTop = scrollTop;

    // request the next animation frame
    requestAnimationFrame(updateScrollPosition);
  }

  // start the animation for each container
  requestAnimationFrame(updateScrollPosition);
});


